/* ‚îÄ‚îÄ Apply Translations ‚îÄ‚îÄ */
let currentLang = 'en';

function applyTranslations(lang) {
  const t = TRANSLATIONS[lang];
  if (!t) return;

  // data-i18n elements (text content)
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (t[key] !== undefined) {
      el.textContent = t[key];
    }
  });

  // data-i18n-html elements (innerHTML ‚Äî for gold spans etc.)
  document.querySelectorAll('[data-i18n-html]').forEach(el => {
    const key = el.getAttribute('data-i18n-html');
    if (t[key] !== undefined) {
      el.innerHTML = t[key];
    }
  });

  // Update lang toggle button
  const langToggle = document.getElementById('langToggle');
  if (langToggle) {
    const labelEl = document.getElementById('langLabel');
    if (labelEl) labelEl.textContent = t['nav.lang'] || '‡∞§‡±Ü‡∞≤‡±Å‡∞ó‡±Å';
  }

  // Update html lang attribute
  document.documentElement.setAttribute('lang', lang === 'te' ? 'te' : 'en');

  // Update body font for Telugu
  if (lang === 'te') {
    document.body.style.fontFamily = "'Noto Sans Telugu', 'Outfit', sans-serif";
  } else {
    document.body.style.fontFamily = "'Outfit', sans-serif";
  }

  currentLang = lang;
}

/* ‚îÄ‚îÄ Toggle Language ‚îÄ‚îÄ */
function toggleLang() {
  const newLang = currentLang === 'en' ? 'te' : 'en';
  applyTranslations(newLang);

  // Save preference
  try {
    localStorage.setItem('mvrp-lang', newLang);
  } catch (e) {}
}

/* ‚îÄ‚îÄ Init Language on Load ‚îÄ‚îÄ */
document.addEventListener('DOMContentLoaded', () => {
  // Try to restore saved language
  let savedLang = 'en';
  try {
    savedLang = localStorage.getItem('mvrp-lang') || 'en';
  } catch (e) {}

  if (savedLang === 'te') {
    applyTranslations('te');
  }
});
